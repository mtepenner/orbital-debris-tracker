import (
	"context"
	"os"
)

func FetchCatalog(ctx context.Context) (string, error) {
	_ = ctx
	if raw := os.Getenv("SPACETRACK_SAMPLE_CATALOG"); raw != "" {
		return raw, nil
	}
	return sampleCatalog(), nil
}

